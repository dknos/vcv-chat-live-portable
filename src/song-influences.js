function normalizeTitle(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

function cloneProgressions(progressions) {
  return Object.fromEntries(
    Object.entries(progressions).map(([section, progression]) => [
      section,
      progression.map((entry) => ({ ...entry })),
    ]),
  );
}

export function resolveSongInfluence(rawTitle) {
  const requested = normalizeTitle(rawTitle);
  if (!requested) return null;
  const preset = Object.values(SONG_INFLUENCES).find((candidate) =>
    candidate.aliases.some((alias) => normalizeTitle(alias) === requested));
  if (!preset) return null;
  const influenceProgressions = cloneProgressions(preset.progressions);
  const first = influenceProgressions.default[0];
  return {
    id: preset.id,
    title: preset.title,
    artist: preset.artist,
    changes: {
      ...preset.changes,
      influenceId: preset.id,
      influenceTitle: preset.title,
      influenceArtist: preset.artist,
      influenceProgressions,
      currentChordSemitone: first.semitone,
      currentChordQuality: first.quality,
      currentChordBassSemitone: first.bass,
    },
  };
}

export function songInfluenceNames() {
  return Object.values(SONG_INFLUENCES).map(({ title }) => title);
}

